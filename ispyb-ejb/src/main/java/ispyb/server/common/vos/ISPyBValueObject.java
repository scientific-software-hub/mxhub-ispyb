package ispyb.server.common.vos;

import java.io.Serializable;

/**
 * This abstract base class defines the methods all SMIS value objects have to implement.
 */
abstract public class ISPyBValueObject implements Serializable {

	private static final long serialVersionUID = 5023767168091986946L;

	/**
	 * Checks the values of this value object for correctness and completeness. Should be done before persisting the
	 * data in the DB.
	 * 
	 * @param create
	 *            should be true if the value object is just being created in the DB, this avoids some checks like
	 *            testing the primary key
	 * @throws VOValidateException
	 *             if the data of the value object is not correct
	 */
	abstract public void checkValues(boolean create) throws Exception;

}